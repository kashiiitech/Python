#This code will take list as a parameter and gives sum of all elements

def sum_list(x):
    y = 0
    for i in x:
        y = y + i
    return y
    
    
##This code will take list as a parameter and gives product of all elements


def product_list(x):
    y = 1
    for i in x:
        y = y * i
    return y
